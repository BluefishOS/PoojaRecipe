$(window).load(function(){
    $(".recipe-modal").click(function(){
        var modalTitle = $(this).attr('modal-data');
        var data = ModalData.find(x => x.title === modalTitle);
        $('#SiteModalLabel').html(modalTitle);
        $("#preparation, #ingredient, #cooking, #tipstrick").empty();
        fillData(data.preparation, '#preparation');
        fillData(data.ingredient, '#ingredient');
        fillData(data.cooking, '#cooking');
        fillData(data.tips, '#tipstrick');
        $('#SiteModal').modal('show');
        return false;
    });
    
});

function fillData(sourceData, elementId)
{
    sourceData.forEach(src => {     
        $(elementId).append("<h3>"+src.title+"</h3>");
        $(elementId).append("<ul></ul>");
        src.data.forEach(data => {
            $(elementId+" ul:last").append("<li>"+data+"</li>");
        });
    });
}